package com.techymeet.crm.bo;

public class ProductBo {
	private int productId;
	private String productName;
	private String productSpecification;
	private String minimumStocks;
	private String maximumStocks;
	private String availableStocks;
	private int price;
	
	public int getProductId() {
		return productId;
	}

	public void setProductId(int productId) {
		this.productId = productId;
	}

	

	public String getProductName() {
		return productName;
	}

	public void setProductName(String productName) {
		this.productName = productName;
	}

	public String getProductSpecification() {
		return productSpecification;
	}

	public void setProductSpecification(String productSpecification) {
		this.productSpecification = productSpecification;
	}

	public String getMinimumStocks() {
		return minimumStocks;
	}

	public void setMinimumStocks(String minimumStocks) {
		this.minimumStocks = minimumStocks;
	}

	public String getMaximumStocks() {
		return maximumStocks;
	}

	public void setMaximumStocks(String maximumStocks) {
		this.maximumStocks = maximumStocks;
	}

	public String getAvailableStocks() {
		return availableStocks;
	}

	public void setAvailableStocks(String availableStocks) {
		this.availableStocks = availableStocks;
	}

	public int getPrice() {
		return price;
	}

	public void setPrice(int price) {
		this.price = price;
	}

	

	

}
